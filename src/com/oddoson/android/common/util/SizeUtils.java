package com.oddoson.android.common.util;

/**
 * SizeUtils
 * 字节转换、
 * 时间转换
 */
public class SizeUtils {

    /** gb to byte **/
    public static final long GB_2_BYTE = 1073741824;
    /** mb to byte **/
    public static final long MB_2_BYTE = 1048576;
    /** kb to byte **/
    public static final long KB_2_BYTE = 1024;
    /** 秒 to 毫秒 **/
    public static final int SECOND_2_MILLISCOND = 1000;
    /** 分  to 毫秒 **/
    public static final int MINUTE_2_MILLISCOND = 60000;
    /** 小时 to 毫秒 **/
    public static final int HOUR_2_MILLISCOND = 3600000;
    /** 天 to 毫秒 **/
    public static final int DAY_2_MILLISCOND = 86400000;
}
